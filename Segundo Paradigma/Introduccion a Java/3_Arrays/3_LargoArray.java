/* Largo Array
Esto es totalmente trivial, no como C :)
Podemos acceder al largo de un array de forma simple, ingresando .length
Asi pues en el codigo

-REcordar que los arrays comienzan desde el 0-
*/


class LargoArray{
    public static void main(String[] args){
        int[] intArr = new int[5]; //Creamos un array de 5 elementos
        System.out.println(intArr.length); //Imprimimos unicamente su largo, el cual es 5
    }
}